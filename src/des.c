__uint64_t expansion(__uint32_t R) {
    __uint64_t e = 0x00;
    __uint64_t value = R;

    e = ((value & 0x1) << 47) | ((value & 0xF8000000) << 15) | ((value & 0x1F800000) << 13) |
        ((value & 0x1F80000) << 11) | ((value & 0x1F8000) << 9) | ((value & 0x1F800) << 7) |
        ((value & 0x1F80) << 5) | ((value & 0x1F8) << 3) | ((value & 0x1F) << 1) | 
        ((value & 0x80000000) >> 31);

    return e;
}

void get_L16_R16(__uint64_t value, __uint32_t *L16, __uint32_t *R16) {
    *L16 = (value >> 32) & 0xFFFFFFFF;
    *R16 = value & 0xFFFFFFFF;
}
